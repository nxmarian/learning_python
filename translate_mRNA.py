# Make a program that translates mRNA into protein
# It should report the longest ORF in each mRNA
# Make sure you translate all 3 reading frames on both strands
# It should use argparse and your library, of course

# make reverse of each sequence
# add it into sequence
# look for longest orf in seq
# translate it into amino acids

import sys
import mcb185
import argparse

#Setup
parser = argparse.ArgumentParser(description='translates mRNA to amino acids')
# required arguments
parser.add_argument('--fasta', required=True, type=str,
	metavar='<str>', help='put fasta file')

arg = parser.parse_args()	


maxorf = [] #empty list to put longest orf in
for name, seq in mcb185.read_fasta(arg.fasta):
	seq = mcb185.orfseq(seq) 
	reverse = mcb185.reverse(seq)
	reverse_seq = mcb185.orfseq(reverse) #reverse_seq is the reverse of orfseq(seq)
	seq += reverse_seq #add that reversed sequence into the sequence
	seq.sort(key=len, reverse=True) 
	#key=len means to sort by length, reverse=True means to put longest to shortest
	maxorf = seq[0] #maxorf is gonna be the first sequence after sorting
	
	print(name, mcb185.translate(maxorf))
	

